namespace SensorDataChallenge.Enums
{
    public enum PermissionEnum
    {
        ViewUsers = 1, // Permission to view users in the system
        CreateUser, // Permission to register a user in the system
        UpdateUser, // Permission to modify a user in the system
        DeleteUser, // Permission to unsubscribe a user from the system
        ViewClients, // Permission to view clients in the system
        CreateClient, // Permission to register a client in the system
        UpdateClient, // Permission to modify a client in the system
        DeleteClient, // Permission to remove a client from the system
        ViewMap  // Permission to view the map
    }
}
